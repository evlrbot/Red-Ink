<?php
class Visualization extends Model {
/********************************************************************************
 *                                 CONSTRUCTOR
 ********************************************************************************/
  function Visualization() {
    parent::Model();
    $this->load->database();
  }

/********************************************************************************
 *                                WRITE METHODS
 ********************************************************************************/

/********************************************************************************
 *                                ACCESSOR METHODS
 ********************************************************************************/

  function get_visualizations() {
    $query = "SELECT * FROM public.view";
    $result = $this->db->query($query);
    return $result->result_array();
  }
  
}